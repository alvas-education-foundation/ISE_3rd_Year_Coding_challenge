S = []
I = []

for i in range(n):
    ele = input()
    S.append(ele)
for i in range(n):
    ele = int(input())
    I.append(ele)

for i in range(n):
    print(S[i],"\t\t   ",end="")
    if I[i]<10:
        print("00",end="")
    elif I[i]>9 and I[i]<100:
        print("0",end="")
    print(I[i])







































































































































































































































